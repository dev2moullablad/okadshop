	<h1>Livraison et retours</h1>
	<div class="rte">
    <h3>Le transport de votre colis</h3>
    <p>Les colis sont généralement expédiés en 48h après réception de votre paiement. Le mode d'expédition standard est le Colissimo suivi, remis sans signature. Si vous souhaitez une remise avec signature, un coût supplémentaire s'applique, merci de nous contacter. Quel que soit le mode d'expédition choisi, nous vous fournirons dès que possible un lien qui vous permettra de suivre en ligne la livraison de votre colis.</p>
    <p>Les frais d'expédition comprennent l'emballage, la manutention et les frais postaux. Ils peuvent contenir une partie fixe et une partie variable en fonction du prix ou du poids de votre commande. Nous vous conseillons de regrouper vos achats en une unique commande. Nous ne pouvons pas grouper deux commandes distinctes et vous devrez vous acquitter des frais de port pour chacune d'entre elles. Votre colis est expédié à vos propres risques, un soin particulier est apporté au colis contenant des produits fragiles..<br>
    <br>
    Les colis sont surdimensionnés et protégés.</p>
